#include <cmath>
#include <iostream>

int main() {
    std::cout << "12 + 4 = " << 12 + 4 << std::endl;
    std::cout << "3 - 1 = " << 3 - 1 << std::endl;
    std::cout << "49 / 7 = " << 49 / 7 << std::endl;
    std::cout << "24 - 12 / 6 = " << 24 - 12 / 6 << std::endl;
    std::cout << "4^6 = " << pow(4, 6) << std::endl;
    std::cout << "3 / 9 = " << float(3) / float(9) << std::endl;
}
